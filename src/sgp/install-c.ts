function InstallApplication(strAppname: STR, strPath: STR): BOOLEAN {
  let hKey: HKEY;
  let fRet: BOOL = TRUE;

  hKey = GetAppRegistryKey();
  RegCloseKey(hKey);

  // hKeySection = GetSectionKey("Startup");
  // RegCloseKey( hKeySection );

  fRet = fRet && WriteProfileChar("Startup", "InstPath", strPath);

  return fRet;
}
